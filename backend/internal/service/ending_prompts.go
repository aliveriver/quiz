func normalizeEndingID(endingID string) string {
	switch strings.ToUpper(strings.TrimSpace(endingID)) {
	case "A", "B", "C", "D", "E", "F":
		return strings.ToUpper(strings.TrimSpace(endingID))
	default:
		return "E"
	}
}

